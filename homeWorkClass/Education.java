package homeWorkClass;

public class Education {
	int id;
	String name;
	String detail;
	String instructor;
	
	public Education() {
		
	}
	
	public Education(int id, String name, String detail, String instructor) {
		this();
		this.id = id;
		this.name = name;
		this.detail = detail;
		this.instructor = instructor;
	}
}
